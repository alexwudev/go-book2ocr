// RTL languages
const rtlLangs = new Set(['fa']);

/**
 * Get translation for a key, with optional interpolation.
 * Usage: t('msg.loadedImages', { count: 5 })
 */
export function t(key, params) {
    const dict = T[currentLang] || T['en'];
    let str = dict[key];
    if (str === undefined) {
        // Fallback to English, then to key itself
        str = (T['en'] && T['en'][key]) || key;
    }
    if (params) {
        for (const [k, v] of Object.entries(params)) {
            str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
        }
    }
    return str;
}

/** Get current UI language code */
export function getCurrentLang() {
    return currentLang;
}

/** Check if current language is RTL */
export function isRTL() {
    return rtlLangs.has(currentLang);
}

/**
 * Set UI language and apply translations to the DOM.
 * Does NOT save to config — caller should handle persistence.
 */
export function setLanguage(lang) {
    if (!T[lang]) lang = 'en';
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = isRTL() ? 'rtl' : 'ltr';
    applyTranslations();
}

/** Apply translations to all [data-i18n], [data-i18n-title], [data-i18n-placeholder] elements */
export function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
}
